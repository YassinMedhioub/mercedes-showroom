export function useI18n() {
    const { lang } = usePrefs()

    const currentLang = computed(() => {
        if (lang.value === 'EN' || lang.value === 'FR') return lang.value
        return 'FR'
    })

    const t = (key) => {
        return translations[currentLang.value]?.[key] ||
            translations.FR[key] ||
            key
    }

    return { t, currentLang }
}